    public Booking_History(String ID, boolean Customer) {
        initComponents();
        this.isCustomer = Customer;
        if(this.isCustomer)
            this.cancelBtn.setEnabled(true);
        
        DefaultTableModel model = (DefaultTableModel)historyTbl.getModel();
        bookings = files.ReadFromFile("Booking.txt");
        for(int i = 0; i < bookings.size(); i++)
        {
            if(bookings.get(i)[1].equals(ID))
            {
                Object[] row = bookings.get(i);
                model.addRow(row);
            }
        }
    }

