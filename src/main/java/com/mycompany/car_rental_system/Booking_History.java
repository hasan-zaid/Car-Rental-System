    private void cancelBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelBtnActionPerformed
        DefaultTableModel model = (DefaultTableModel)historyTbl.getModel();
        if(historyTbl.getSelectedRow() >= 0)
        {
            if(model.getValueAt(historyTbl.getSelectedRow(), 7).equals("canceled"))
            {
                this.warningMessageLbl.setText("Booking has already been canceled");
                this.warningMessageLbl.setForeground(Color.red);
            }
            else{
                if(customer.cancelBooking(model, historyTbl.getSelectedRow()))
                {
                    this.warningMessageLbl.setText("Booking has been canceled successfully");
                    this.warningMessageLbl.setForeground(Color.green);
                }
                else{
                    this.warningMessageLbl.setText("Your booking is no longer pending and therefore cannot be canceled !");
                    this.warningMessageLbl.setForeground(Color.red);
                    }
                }
        }
        else{
            this.warningMessageLbl.setText("Please select a booking first !");
            this.warningMessageLbl.setForeground(Color.red);
        }
    }//GEN-LAST:event_cancelBtnActionPerformed

