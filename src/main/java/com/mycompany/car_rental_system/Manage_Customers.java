    private void customersTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customersTblMouseClicked
        DefaultTableModel model = (DefaultTableModel)customersTbl.getModel();
        Object status = model.getValueAt(customersTbl.getSelectedRow(), 4);
        if(status.equals("banned")){
            this.actionBtn.setText("Activate");
            this.actionBtn.setEnabled(true);
        }
        else if(status.equals("active"))
        {
            this.actionBtn.setText("Ban");
            this.actionBtn.setEnabled(true);
        }
        else
            this.actionBtn.setEnabled(false);
    }//GEN-LAST:event_customersTblMouseClicked

