    private void actionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actionBtnActionPerformed
             DefaultTableModel model = (DefaultTableModel)customersTbl.getModel();
             int selectedIndex = customersTbl.getSelectedRow();
             if(selectedIndex >= 0)
             {
                 Object status = customersTbl.getValueAt(selectedIndex, 4);
                 this.rows = files.getData();
                 if(status.equals("banned"))
                 {
                     model.setValueAt("active", selectedIndex, 4);
                     this.actionBtn.setText("Ban");
                     this.rows.get(selectedIndex)[4] = "active";
                 }
                 else{
                     model.setValueAt("banned", selectedIndex, 4);
                     this.actionBtn.setText("activate");
                     this.rows.get(selectedIndex)[4] = "banned";
                 }

                 files.editFile(rows, fileName);
                 this.warningMessageLbl.setText("Custoemr account status has been updated successfully ");
                 this.warningMessageLbl.setForeground(Color.green);
             }
             else{
                 this.warningMessageLbl.setText("No row is selected !");
                 this.warningMessageLbl.setForeground(Color.red);
             }
    }//GEN-LAST:event_actionBtnActionPerformed

